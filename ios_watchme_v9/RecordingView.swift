                }
            } else {
                print("‚ùå ‰∏ÄÊã¨„Ç¢„ÉÉ„Éó„É≠„Éº„ÉâÂ§±Êïó: \(recording.fileName)")
            }
            
            // ÊàêÂäü„ÉªÂ§±Êïó„Å´„Åã„Åã„Çè„Çâ„Åö„ÄÅÊ¨°„ÅÆ„Éï„Ç°„Ç§„É´„ÅÆ„Ç¢„ÉÉ„Éó„É≠„Éº„Éâ„ÇíÂÜçÂ∏∞ÁöÑ„Å´Âëº„Å≥Âá∫„Åô
            self.uploadSequentially(recordings: remainingRecordings)
        }
    }
}

