    private func getTopEmotion(_ emotionReport: EmotionReport) -> (String, Int) {
        let totals = emotionReport.emotionTotals
        let emotions = [
            ("Joy", totals.joy),
            ("Trust", totals.trust),
            ("Fear", totals.fear),
            ("Surprise", totals.surprise),
            ("Sadness", totals.sadness),
