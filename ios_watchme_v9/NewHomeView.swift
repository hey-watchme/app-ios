    // MARK: - Data Refresh
    private func refreshData(for date: Date) {
        guard !isRefreshing else { return }
        
        isRefreshing = true
        Task {
            // „Ç≠„É£„ÉÉ„Ç∑„É•„Çí„ÇØ„É™„Ç¢„Åó„Å¶Âº∑Âà∂ÁöÑ„Å´Êñ∞„Åó„ÅÑ„Éá„Éº„Çø„ÇíÂèñÂæó
            await forceRefreshData(for: date)
            
            await MainActor.run {
                isRefreshing = false
                lastRefreshDate = date
            }
        }
    }
    
    private func forceRefreshData(for date: Date) async {
        guard let deviceId = viewModel.deviceManager.selectedDeviceID ?? viewModel.deviceManager.localDeviceIdentifier else {
            return
        }
        
        // „Éá„Éê„Ç§„Çπ„ÅÆ„Çø„Ç§„É†„Çæ„Éº„É≥„ÇíÂèñÂæó
        let timezone = viewModel.deviceManager.getTimezone(for: deviceId)
        
        // Áõ¥Êé•„Éá„Éº„Çø„Éû„Éç„Éº„Ç∏„É£„Éº„ÇíÊõ¥Êñ∞Ôºà„Ç≠„É£„ÉÉ„Ç∑„É•„ÇíÁÑ°Ë¶ñÔºâ
        await viewModel.dataManager.fetchAllReports(
            deviceId: deviceId,
            date: date,
            timezone: timezone
        )
        
        // ViewModel„ÅÆ„Ç≠„É£„ÉÉ„Ç∑„É•„ÇÇÊõ¥Êñ∞„Åï„Çå„Çã„Çà„ÅÜ„Å´„Éà„É™„Ç¨„Éº
        await viewModel.onAppear()
    }
