                    let reason = !recording.fileExists() ? "„Éï„Ç°„Ç§„É´‰∏çÂ≠òÂú®" : 
                                recording.uploadAttempts >= 3 ? "ÊúÄÂ§ßË©¶Ë°åÂõûÊï∞Ë∂ÖÈÅé" : "‰∏çÊòé"
